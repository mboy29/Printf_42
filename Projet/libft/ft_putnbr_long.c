#include "libft.h"

int		ft_putnbr_long(long n)
{
	char	c;
	int		print;

	print = ft_nblen(n);
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n < 10)
	{
		c = (n % 10) + 48;
		write(1, &c, 1);
	}
	else
	{
		ft_putnbr_long(n / 10);
		c = (n % 10) + 48;
		write(1, &c, 1);
	}
	return (print);
}
