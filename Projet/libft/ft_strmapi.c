#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	int		len;
	char	*dst;

	i = 0;
	len = ft_strlen(s);
	if (!s || !f)
		return (NULL);
	if (!(dst = malloc(sizeof(char) * (len + 1))))
		return (NULL);
	while (s[i])
	{
		dst[i] = f(i, s[i]);
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
