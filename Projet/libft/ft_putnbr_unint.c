#include "libft.h"

int		ft_putnbr_unint(unsigned int n)
{
	char c;

	if (n < 10)
	{
		c = (n % 10) + 48;
		write(1, &c, 1);
	}
	else
	{
		ft_putnbr_unint(n / 10);
		c = (n % 10) + 48;
		write(1, &c, 1);
	}
	return (ft_nblen(n));
}
